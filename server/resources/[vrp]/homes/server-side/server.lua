					if ok then
						if vRP.paymentBank(user_id,parseInt(homes[tostring(homeName)][2])) then
							vRP.execute("vRP/buy_permissions",{ home = tostring(homeName), user_id = parseInt(user_id), vault = parseInt(homes[tostring(homeName)][4]) })
						else
							TriggerClientEvent("Notify",source,"vermelho","Dólares insuficientes.",3000)
						end
					end
					return false
				end
			end
		end
	end
