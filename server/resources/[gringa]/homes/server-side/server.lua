function cRP.openChest(homeName)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local hmInventory = {}
		local myInventory = {}

		local data = vRP.getSData("homesVault:"..tostring(homeName))
		local result = json.decode(data) or {}
		if data then
			for k,v in pairs(result) do
				table.insert(hmInventory,{economy = vRP.itemEconomyList(k), unity = vRP.itemUnityList(k), tipo = vRP.itemTipoList(k), color = vRP.itemColor(k), desc = vRP.itemDescList(k), amount = parseInt(v.amount), name = vRP.itemNameList(k), index = vRP.itemIndexList(k), key = k, peso = vRP.itemWeightList(k) })
			end
		end

		local inv = vRP.getInventory(user_id)
		if inv then
			for k,v in pairs(inv) do
				if string.sub(v.item,1,9) == "toolboxes" then
					local advFile = LoadResourceFile("logsystem","toolboxes.json")
					local advDecode = json.decode(advFile)

					v.durability = advDecode[v.item]
				end
				if v.item and v.timestamp then
						local actualTime = os.time()
						local finalTime = v.timestamp
						local durabilityInSeconds = vRP.itemDurabilityList(v.item)
						local startTime = (v.timestamp - durabilityInSeconds)
						
						local actualTimeInSeconds = (actualTime - startTime)
						local porcentage = (actualTimeInSeconds/durabilityInSeconds)-1
						if porcentage < 0 then porcentage = porcentage*-1 end
						if porcentage <= 0.0 then
							porcentage = 0.0
						elseif porcentage >= 100.0 then
							porcentage = 100.0
						end
						if porcentage then
							v.durability = porcentage
						end
					end

				v.amount = parseInt(v.amount)
				v.name = vRP.itemNameList(v.item)
				v.desc = vRP.itemDescList(v.item)
				v.tipo = vRP.itemTipoList(v.item)
				v.color = vRP.itemColor(v.item)
				v.unity = vRP.itemUnityList(v.item)
				v.economy = vRP.itemEconomyList(v.item)
				v.peso = vRP.itemWeightList(v.item)
				v.index = vRP.itemIndexList(v.item)
				v.key = v.item
				v.slot = k

				myInventory[k] = v
			end
		end

		local identity = vRP.getUserIdentity(user_id)
		if identity then
			local vWeight = vRP.query("vRP/get_homeuseridowner",{ home = tostring(homeName) })
			if vWeight[1] then
				return myInventory,hmInventory,vRP.computeInvWeight(user_id),vRP.getBackpack(user_id),vRP.computeChestWeight(result),parseInt(vWeight[1].vault),{ identity.name.." "..identity.name2,parseInt(user_id),identity.phone,identity.registration }
			end
		end
	end
	
	return false
end
