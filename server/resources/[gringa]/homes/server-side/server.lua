				local homeResult = vRP.query("vRP/get_homepermissions",{ home = tostring(homeName) })
				if parseInt(#homeResult) >= 1 then
					local myResult = vRP.query("vRP/get_homeuser",{ user_id = parseInt(user_id), home = tostring(homeName) })
					if myResult[1] then
						return true
					else
						if not unlocked[homeName] then
							TriggerClientEvent("Notify",source,"amarelo","A porta está trancada.",3000)
							return false
						else
							return true
						end
					end
				else
					local getInvoice = vRP.getInvoice(user_id)
					if getInvoice[1] ~= nil then
						TriggerClientEvent("Notify",source,"amarelo","Encontramos faturas pendentes.",3000)
						return
					end

					local getFines = vRP.getFines(user_id)
					if getFines[1] ~= nil then
						TriggerClientEvent("Notify",source,"amarelo","Encontramos multas pendentes.",3000)
						return
					end

					local totalHomes = vRP.query("vRP/count_homes",{ user_id = parseInt(user_id) })
					if vRP.getPremium(parseInt(user_id)) then
						if parseInt(totalHomes[1].qtd) >= 4 then
							TriggerClientEvent("Notify",source,"vermelho","Você atingiu o máximo de residências.",3000)
							return false
						end
					else
						if parseInt(totalHomes[1].qtd) >= 2 then
							TriggerClientEvent("Notify",source,"vermelho","Você atingiu o máximo de residências.",3000)
							return false
						end
					end

					local ok = vRP.request(source,"Comprar propriedade("..homeName..") por <b>$"..vRP.format(parseInt(homes[tostring(homeName)][2])).." dólares</b>?",60)
					if ok then
						if vRP.paymentBank(user_id,parseInt(homes[tostring(homeName)][2])) then
							vRP.execute("vRP/buy_permissions",{ home = tostring(homeName), user_id = parseInt(user_id), vault = parseInt(homes[tostring(homeName)][4]) })
						else
							TriggerClientEvent("Notify",source,"vermelho","Dólares insuficientes.",3000)
						end
					end
					
					return false
				end
			end
		end
	end
	
	return false
end
